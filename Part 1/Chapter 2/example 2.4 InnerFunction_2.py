"""
常用内置函数：
    abs()：取模(绝对值)；
    pow(x,y)：x的y次方，等价于x**y；
    round(x[,小数位数]) : 保留小数，不给出小数位数返回整数，功能非常接近于四舍五入

"""

print(abs(-5))
print(abs(1 + 1j))

print(pow(2, 3))
print(pow(2, -3))

print(round(5.923))
print(round(-5.923))


print(round(21.55, 1))
print(round(-21.55, 1))
print(round(21.55, -1))
print(round(-21.55, -1))




